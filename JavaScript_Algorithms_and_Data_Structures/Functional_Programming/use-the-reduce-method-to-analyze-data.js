// reduce() 사용해서 "Christopher Nolan" 감독 영화의 평균평점을 구하기.

function getRating(watchList) {
  
  let averageRating;

  // filter()로 조건에 해당하는 아이템만 거르기. 
  averageRating = watchList.filter((item) => item.Director === "Christopher Nolan");
  
  let items = averageRating.length;
  
  // reduce() 로 평점의 합 구하기.
  averageRating = averageRating.reduce((acc, curr) => {
    return acc + parseFloat(curr["imdbRating"]);
  }, 0);

  return averageRating / items;
}

